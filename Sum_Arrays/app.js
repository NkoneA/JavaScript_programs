//Declare a function that takes array as a parameter
function sumArray(arr){
//Declare a variable and initialize it to 0 to keep count
    let sum = 0;
//for loop to iterate through each element of the array
    for(let i = 0; i < arr.length; i++);
//Update the sum variable
        sum += arr[i];

    }
//Return sum
    return sum;

